#!/usr/bin/python3
def no_c(my_string):
    new_string = ""  # empty string to store new_string

    for char in my_string:  # iterate through each character in my_string
        if char != 'c' and char != 'C':  # check for c and C
            new_string += char  # if no c or C, add character to new_string

    return new_string
